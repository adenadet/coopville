<div id="procedures" class="tab-pane fade">
    <h3>LOAN PROCEDURES</h3>
    <p>Every member is qualified to get a loan from the cooperative scheme provide that such a member meets up with the loan conditions.<ul>
        <li>Obtain a form from the loans committee with a non-refundable fee of &#x20A6; 200.</li>
        <li>Complete the form and submit it to the loan committee through your unit.</li>
        <li>Loan forms must be endorsed by the unit chairman and the loan committee chairman.</li>
        <li>The duly signed loan form shall be distributed accordingly.</li>
    </ul></p>
</div>
<div id="conditions" class="tab-pane fade">
    <h3>LOAN CONDITIONS</h3>
    <ul>
        <li>Interest rate is 5% annum - subject to review.</li>
        <li>Maximum loan is 200% of total savings.</li>
        <li>Maximum repayment period is 18months.</li>
        <li>An extra charge will be collected on loan defaulters.</li>
        <li>The required guarantors must be contributing members.</li>
        <li>A minimum of 1 and maximum of 5 guarantors are required.</li>
        <li>Loans are disbursed based on availability of funds.</li>
        <li>Application for loan shall be processed within 14 days, on first-come first-served.</li>
        <li>Applicant must have saved for a minimum of 6 months.</li>
        <li>Guarantors must have saved for a minimum of 6 months.</li>
    </ul>
