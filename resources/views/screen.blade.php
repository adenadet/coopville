<div id="savings" class="tab-pane fade">
    <h3>MEMBERSHIP SAVINGS</h3>
    <p><ul>
        <li>Membership registration is N1,000.</li>
        <li>An identification card pass book and DLC Ikeja membership form is given.</li>
        <li>Every member has a unique identification code upon registration that you must add to your name whenever you make payments.</li>
        <li>Every member makes monthly savings as determined by the member.</li>
        <li>The monthly savings must be in multiplies of N1,000.</li>
        <li>A form is obtained from the Treasurer should a member decides to increase or reduce his/her monthly equal savings (April and October).</li>
        <li>A member can terminate his/her membership by given notice through the unit coordinator.</li>
        <li>Membership termination attracts no fees.</li>
        <li>Any withdrawal of savings by a member will be treated as a special loan.</li>
        <li>Membership is open to every interested parishioner.</li>
    </ul></p>
